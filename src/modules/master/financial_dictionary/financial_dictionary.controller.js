"use strict";
const service = require("./financial_dictionary.service");
const { asyncHandler, AppError } = require("../../../utils/errors");
const actor = (req) => req.user || { user_id: null };
module.exports = {
  list: asyncHandler(async (req, res) => res.json({ data: await req.tenantDb((c) => service.listItems(c, req.query)) })),
  get: asyncHandler(async (req, res) => { const r = await req.tenantDb((c) => service.get(c, req.params.id)); if (!r) throw new AppError("NOT_FOUND", "Dictionary item not found", 404); res.json({ data: r }); }),
  create: asyncHandler(async (req, res) => res.status(201).json({ data: await req.tenantDb((c) => service.create(c, { data: req.body, actor: actor(req) })) })),
  update: asyncHandler(async (req, res) => { const r = await req.tenantDb((c) => service.update(c, { id: req.params.id, patch: req.body, actor: actor(req) })); if (!r) throw new AppError("NOT_FOUND", "Dictionary item not found", 404); res.json({ data: r }); }),
};
